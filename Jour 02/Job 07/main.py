def afficher_type_developpeur(langage):
    if langage == "javascript":
        print("tu es un developpeur web")
    elif langage == "python":
        print("tu es un developpeur IA")
    elif langage == "java":
        print("tu es un developpeur logiciel")
    elif langage == "reactjs":
        print("tu es un developpeur mobile")
    else:
        print("un jour, je serai le meileur developpeur")
afficher_type_developpeur("javascript")
afficher_type_developpeur("python")
afficher_type_developpeur("java")
afficher_type_developpeur("reactjs")
afficher_type_developpeur("un jour, je serai le meilleur developpeur")